package loop;

public class ForLoop {

	public static void main(String[] args) 
	{
		//     another type of loop 
		
//             syntax:    
//  		          1                 2 ,5,8                 4,7
//           for  (any_java_statement: boolean_condition;  any_java_statement)
   //      {
	//              3,6,9
	//	       action to be executed 
	//     }
		int a = 5;
		for (  System.out.println( " starting the loop ");  a<10;  a++ )            
		{
			System.out.println(" executing the for loop ");
			
			// note - we can understand the sequence of execution using the syntax 
			
      }
		
		
		
		
		
		
	}

}
